#Regional Analysis
use ShopSmart_Retail_Analytics;

#Which market generates the highest revenue?
with market_revenue as
(select l.market,
round(sum(sales),2) as total_revenue,
round(sum(profit),2) as total_profit
from location l
inner join fact_sales f
using(location_id)
group by l.market)
select market,
total_revenue,
total_profit,
round((total_revenue/
           (select sum(total_revenue) from market_revenue))*100,2) as revenue_percent
from market_revenue
order by total_revenue desc;

#Which region contributes the highest revenue?
select l.region, round(sum(sales),2) as total_revenue,
round(sum(profit),2) as total_profit
from location l
inner join fact_sales f
using(location_id)
group by l.region
order by total_revenue desc;

#Which 10 countries generate the highest revenue
select l.country, round(sum(sales),2) as total_revenue,
round(sum(profit),2) as total_profit
from location l
inner join fact_sales f
using(location_id)
group by l.country
order by total_revenue desc
limit 10;

