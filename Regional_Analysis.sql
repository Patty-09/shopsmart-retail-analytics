#What is the average revenue generated by each country?
select l.country,
round(avg(sales),2) as avg_transaction_revenue
from location l
inner join fact_sales f
using(location_id)
group by l.country
order by avg_transaction_revenue desc
limit 10;

#Rank markets based on total revenue.
with market_revenue as
(select l.market, sum(sales) as total_revenue
      from location l
      inner join fact_sales f 
      using(location_id)
      group by l.market)
select market, total_revenue,
	  dense_rank() over(order by total_revenue desc) as revenue_rnk
from market_revenue;

#Identify regions whose total revenue is above the average regional revenue.
with region_revenue as 
    (select l.region, 
    sum(f.sales) as total_revenue
    from location l
    inner join fact_sales f
    using(location_id)
    group by l.region)
select region, total_revenue
from region_revenue
where total_revenue >(select avg(total_revenue)
                       from region_revenue)
order by total_revenue desc;


#What percentage of the company's total revenue comes from each market?
with market_revenue as 
     (
     select l.market, 
     round(sum(sales),2) as total_revenue
     from location l
     inner join fact_sales f
     using(location_id)
     group by l.market)
select market, total_revenue,
round((total_revenue/ 
       (select sum(total_revenue) from market_revenue))*100,2) as market_perc_contribution
from market_revenue
order by market_perc_contribution desc;



